/*
关系运算符和比较运算符:==, !=, >, <, >=, <= 
*/

#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    int a=3,b=2;
    cout << "a>b:"<< (a>b) <<endl;
    cout << "a<b:"<< (a<b) <<endl;
    cout << "a==b:"<< (a==b) <<endl;
    cout << "a!=b:"<< (a!=b) <<endl;
	return 0;
}

/*
a>b:1
a<b:0
a==b:0
a!=b:1
*/